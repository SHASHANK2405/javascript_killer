
// FOR LOOP
for(let i=0 ; i<=10 ;i++){
    const elem = i;
    if(elem ==5) {
        console.log("5 is best num");
    }
}

//WHILE LOOP
let i=0;
while(i<10){
    console.log(`Index no. is ${i}`);
    i = i+2;
}

//DO WHILE LOOP
let score = 0;
do {
    console.log(`Score is ${score}`);
    score++;
} while (score <= 10);